package PO;

public class Authority {
	private int AID;
	private String ACode;
	private String AName;
	public int getAID() {
		return AID;
	}
	public void setAID(int aID) {
		AID = aID;
	}
	public String getACode() {
		return ACode;
	}
	public void setACode(String aCode) {
		ACode = aCode;
	}
	public String getAName() {
		return AName;
	}
	public void setAName(String aName) {
		AName = aName;
	}
	
	

}
